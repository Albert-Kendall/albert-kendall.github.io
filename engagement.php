                <h1>Engagement</h1>
                <h2>Where the wedding planning began.</h2>
                
                <p>Albert and Kendall were engaged in Kolkata India on the afternoon of February 24th, 2007. Albert had done a great deal of planning to make sure that he created the perfect moment and the perfect surprise. Everyone was in on the secret, except Kendall!</p>
				<p>We originally met while working at the Disney-MGM Studios in 2002. Albert was working there on the College Program while Kendall was working part-time for the summer. After Albert returned to his home in Southern Illinois and Kendall went to the University of Florida in Gainsville, we remained in contact. It wasn't until 2 years later that we started dating long distance. Another 4 months, and Albert moved to Florida. We've been together in Orlando ever since!</p>
				<p>In September 2006 we decided to go on a mission trip with our church to India departing in December. The trip went through many twists and turns and finally was cancelled due to passport problems and rescheduled for a later date. We were very excited to finally get on the plane and make the trip around the world to India at the end of February the following year. Of course, Kendall didn't know that Albert was planning to propose, but everyone else did!</p>
